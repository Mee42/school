class StatsData{
    public final int iqMin;
    public final int iqMax;
    public final int strengthMin;
    public final int strengthMax;
    StatsData(int iqMin,int iqMax, int strengthMin, int strengthMax){
        this.iqMin = iqMin;
        this.iqMax = iqMax;
        this.strengthMin = strengthMin;
        this.strengthMax = strengthMax;
    }
}